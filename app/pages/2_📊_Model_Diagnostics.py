# ── Headline metrics ─────────────────────────────────────────────────────────
sil = float(metrics.get("silhouette_score", 0))
db = float(metrics.get("davies_bouldin", 0))
ch = float(metrics.get("calinski_harabasz", 0))
k = metrics.get("k", "—")
stab = metrics.get("stability", {}) or {}
ari_min = stab.get("ari_min", 0)
ari_mean = stab.get("ari_mean", 0)

