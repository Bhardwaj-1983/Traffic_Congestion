c1, c2 = st.columns(2, gap="medium")
with c1:
    sizes = metrics.get("cluster_sizes", {}) or {}
    if sizes:
        levels = sorted([int(k_) for k_ in sizes.keys()])
        size_vals = [sizes[str(lv)] for lv in levels]
        fig = go.Figure(go.Pie(
            labels=[f"Level {lv}" for lv in levels],
            values=size_vals,
            hole=0.55,
            marker=dict(colors=px.colors.qualitative.Set2,
                        line=dict(color=PLOT_BG, width=2)),
            textinfo="label+percent",
            textfont=dict(color="#fff", size=11),
        ))
        fig.update_layout(
            title=dict(text="Cluster size distribution", font=dict(size=14)),
            plot_bgcolor=PLOT_BG, paper_bgcolor=PLOT_BG, font=dict(color=TEXT),
            height=380, margin=dict(l=20, r=20, t=50, b=20),
            showlegend=False,
        )
        st.plotly_chart(fig, use_container_width=True)

with c2:
    cvintra = metrics.get("cvintra_by_level", {}) or {}
    if cvintra:
        levels = sorted([int(k_) for k_ in cvintra.keys()])
        cv_vals = [cvintra[str(lv)] for lv in levels]
        cv_pass = [v < 0.20 for v in cv_vals]
        colors = ["#2ecc71" if p else ("#f39c12" if v < 0.30 else "#e74c3c")
                  for p, v in zip(cv_pass, cv_vals)]
        fig = go.Figure()
        fig.add_trace(go.Bar(
            x=[f"L{lv}" for lv in levels], y=cv_vals,
            marker_color=colors,
            marker_line_color="rgba(255,255,255,0.18)", marker_line_width=1,
            text=[f"{v*100:.1f}%" for v in cv_vals],
            textposition="outside", textfont=dict(color=TEXT, size=10),
        ))
        fig.add_hline(y=0.20, line_color="#f1c40f", line_dash="dash",
                      annotation_text="20% target",
                      annotation_position="top right",
                      annotation_font=dict(color="#f1c40f"))
        fig.update_layout(
            title=dict(text="CVintra (speed) by cluster", font=dict(size=14)),
            xaxis=dict(title="Cluster", color=TEXT),
            yaxis=dict(title="Coefficient of Variation",
                       gridcolor=GRID, color=TEXT, tickformat=".0%"),
            plot_bgcolor=PLOT_BG, paper_bgcolor=PLOT_BG, font=dict(color=TEXT),
            height=380, showlegend=False, margin=dict(l=50, r=20, t=50, b=40),
        )
        st.plotly_chart(fig, use_container_width=True)

# ── Cluster centroids in feature space ──────────────────────────────────────
centroids = metrics.get("centroid_means", {}) or {}
if centroids:
    st.markdown("<div class='section-h'>Cluster centroids in feature space</div>",
                unsafe_allow_html=True)
    st.markdown(
        "<div class='desc'>Each cluster's centroid, in the standardised feature space the model trained on. "
        "This is what each cluster <i>is</i>: read across the row to interpret the cluster.</div>",
        unsafe_allow_html=True,
    )
    rows = []
    for lvl_str, feat_dict in centroids.items():
        row = {"Cluster": f"L{int(lvl_str)}"}
        row.update({k: round(float(v), 3) for k, v in feat_dict.items()})
        rows.append(row)
    cdf = pd.DataFrame(rows).sort_values("Cluster").reset_index(drop=True)
    st.dataframe(cdf, use_container_width=True, hide_index=True)

    # Radar / spider plot of centroids
    feature_keys = [c for c in cdf.columns if c not in ("Cluster",
                                                        "avg_speed_mph", "trip_density")]
    fig = go.Figure()
    for _, row in cdf.iterrows():
        vals = [row[k] for k in feature_keys] + [row[feature_keys[0]]]
        labs = feature_keys + [feature_keys[0]]
        fig.add_trace(go.Scatterpolar(
            r=vals, theta=labs, name=row["Cluster"], fill="toself", opacity=0.45,
        ))
    fig.update_layout(
        title=dict(text="Cluster centroids — radar view", font=dict(size=14)),
        polar=dict(
            bgcolor=PLOT_BG,
            radialaxis=dict(visible=True, gridcolor=GRID, color=TEXT,
                            angle=90, tickfont=dict(size=9)),
            angularaxis=dict(gridcolor=GRID, color=TEXT,
                             tickfont=dict(size=10)),
        ),
        plot_bgcolor=PLOT_BG, paper_bgcolor=PLOT_BG, font=dict(color=TEXT),
        legend=dict(bgcolor="rgba(0,0,0,0)", font=dict(color=TEXT, size=11)),
        height=520, margin=dict(l=20, r=20, t=50, b=20),
    )
    st.plotly_chart(fig, use_container_width=True)

